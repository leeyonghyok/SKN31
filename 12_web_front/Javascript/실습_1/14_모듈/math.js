// =============================================
// 14. 모듈 - math.js
// =============================================
// 모듈(Module)은 JavaScript 코드를 파일 단위로 나누어 관리하는 방식이다.
// 하나의 파일에 모든 코드를 작성하지 않고, 기능이나 역할에 따라
// 여러 파일로 분리한 뒤 필요한 코드를 내보내고 가져와 사용할 수 있다.
//
// 모듈을 사용하면 다음과 같은 장점이 있다.
// - 관련된 코드를 파일별로 분리할 수 있다.
// - 다른 파일에서 동일한 코드를 재사용할 수 있다.
// - 변수와 함수의 이름이 다른 파일의 코드와 충돌하는 것을 줄일 수 있다.
// - 프로그램의 구조를 파악하고 유지보수하기 쉬워진다.
//
// JavaScript의 표준 모듈 시스템을 ES Modules(ESM)라고 한다.
// ES Modules에서는 export 키워드로 값을 내보내고,
// import 키워드로 다른 파일의 값을 가져온다.

// ---------------------------------------------
// 14.1 이름 있는 내보내기 (Named Export)
// ---------------------------------------------
// 이름 있는 내보내기는 변수, 함수, 클래스 등을 지정된 이름으로 내보내는 방식이다.
// 한 파일에서 여러 개의 값을 이름 있는 내보내기로 내보낼 수 있다.

// 선언과 동시에 내보내기
export default function add(a, b) {
  return a + b;
}

export const PI = 3.141592;
// - 이 파일(math.js)은 add 함수와 PI 상수를 각각의 이름으로 내보낸다.

// 이름 있는 내보내기를 할 때 선언과 내보내기를 분리하여 작성할 수도 있다.
function subtract(a, b) {
  return a - b;
}

export { subtract };
// (md 예시: export { add, subtract, PI };
//  위에서 add, PI를 이미 내보냈으므로 여기서는 subtract만 분리해서 내보냈다.)

